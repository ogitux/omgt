This is a web app (PHP/MySQL) for game tournament management, this app can be used with any kind of game from consoles and computers. You only need to have a web server running PHP and MySQL ;)<br />
<br />
<h2>Why OpenSource?</h2>
Because there are many apps for gaming management that really sucks!! So, in my country Chile, many communities needs a tool for their tournaments.<br />
<br />
